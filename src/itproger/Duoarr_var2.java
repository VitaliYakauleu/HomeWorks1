//1 2 3 4 5
public class Duoarr_var2 {
    public static void main(String[] args) {

        //Давайте посмотрим более подробно на этот код.
        //
        //В начале создается класс Main, в котором будет происходить вся работа.
        //В методе main создается двумерный массив array с 5 строками.
        //Затем происходит заполнение массива двойным циклом.
        // Внешний цикл отвечает за перебор строк, а внутренний - за заполнение элементов внутренних массивов.
        //Внутри внутреннего цикла заполняются элементы текущего подмассива значениями от 1 до номера текущей строки + 1.
        // Таким образом, в каждую строку записываются значения от 1 до номера строки.
        //После заполнения массива происходит вывод его содержимого в консоль,
        // сначала перебираются строки, затем элементы внутренних массивов.
        //После запуска программы в консоли появится указанный вами результат.
        //
        //Надеюсь, это объяснение поможет вам лучше понять, как работает этот код!
        int[][] array = new int[5][]; // Создаем двумерный массив с 5 строками

        // Заполняем массив
        for (int i = 0; i < array.length; i++) {
            array[i] = new int[i + 1]; // Создаем подмассив с количеством элементов, равным номеру строки + 1
            for (int j = 0; j <= i; j++) {
                array[i][j] = j + 1; // Заполняем элементы текущего подмассива значениями от 1 до номера текущей строки + 1
            }
        }

        // Выводим массив в консоль
        for (int i = 0; i < array.length; i++) {
            for (int j = 0; j < array[i].length; j++) {
                System.out.print(array[i][j] + " ");
            }
            System.out.println();
        }
    }
}
